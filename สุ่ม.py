import random

# รับรายชื่อจากผู้ใช้
input_names = input("กรุณาใส่รายชื่อทั้งหมด (คั่นด้วยเครื่องหมายจุลภาค): ")

# แปลงรายชื่อเป็นรายการ (list) โดยการแบ่งจากเครื่องหมายจุลภาค
names = input_names.split(',')

# ตรวจสอบว่าในกรณีที่มีรายชื่อมากพอหรือไม่
if len(names) < 200:
    print("จำนวนรายชื่อไม่เพียงพอ! กรุณาใส่รายชื่อมากกว่า 200 รายชื่อ.")
else:
    # สุ่ม 200 รายชื่อจากรายชื่อทั้งหมด
    random_names = random.sample(names, 200)
    
    # แสดงผลลัพธ์
    print("รายชื่อที่สุ่มได้:")
    for name in random_names:
        print(name)
